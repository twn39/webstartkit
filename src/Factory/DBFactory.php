<?php

namespace App\Factory;

use Illuminate\Database\Capsule\Manager as Capsule;

class DBFactory
{
    public static function getDB() {

        $capsule = new Capsule;

        $capsule->addConnection([
            'driver'    => 'mysql',
            'host'      => 'localhost',
            'database'  => 'database',
            'username'  => 'root',
            'password'  => 'password',
            'charset'   => 'utf8',
            'collation' => 'utf8_unicode_ci',
            'prefix'    => '',
        ]);

        $capsule->setAsGlobal();
        return $capsule;
    }
}
